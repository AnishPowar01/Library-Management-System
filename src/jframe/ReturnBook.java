    public void getIssuedBookDetails()
    {
//        boolean isfound = false;
        int bookId = Integer.parseInt(text_BookId.getText());
        int studentId = Integer.parseInt(text_StudentId.getText());
        
        
        try {
            Connection con = DBConnection.getConnection();
            String sql = "select * from issue_book where book_id = ? and student_id = ? and status = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setInt(1, bookId);
            pst.setInt(2, studentId);
            pst.setString(3, "pending");
            
            ResultSet rs = pst.executeQuery();
            
            if (rs.next()) {
//                isfound = true;
                
                lbl_IssueId.setText(rs.getString("id"));
                lbl_BookName.setText(rs.getString("book_name"));
                lbl_studentName.setText(rs.getString("student_name"));
                lbl_issueDate.setText(rs.getString("issue_date"));
                lbl_dueDate.setText(rs.getString("due_date"));
//                lbl_bookError.setText("");
                
            }
            else
            {
//                   lbl_bookError.setText("No Record Found");
                JOptionPane.showMessageDialog(this, "No ISSUE BOOK RECORD FIND");
                    lbl_IssueId.setText("");
                lbl_BookName.setText("");
                lbl_studentName.setText("");
                lbl_issueDate.setText("");
                lbl_dueDate.setText("");
//                   isfound = false;
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
//        return isfound;
        
    }
