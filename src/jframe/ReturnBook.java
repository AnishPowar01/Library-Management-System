    public boolean returnBook()
    {
        boolean isreturn = false;
        int bookId = Integer.parseInt(text_BookId.getText());
        int studentId = Integer.parseInt(text_StudentId.getText());
        
        try {
            Connection con  =  DBConnection.getConnection();
            String sql = "update issue_book set status = ? where student_id = ? and book_id = ? and status = ?";
            PreparedStatement pst =  con .prepareStatement(sql);
            
            pst.setString(1, "returned");
            pst.setInt(2, studentId);
            pst.setInt(3, bookId);
            pst.setString(4, "pending");
            
            int rowCount = pst.executeUpdate();
            
            if (rowCount > 0) {
                
                isreturn = true;
                
            }
            else
            {
                isreturn = false;
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        return isreturn;
    }
    
    
     
     public void updateBookCount()
     {
         int bookId = Integer.parseInt(text_BookId.getText());
         
         try {
             Connection con  = DBConnection.getConnection();
             String sql = "update book_details set quantity = quantity + 1 where book_id = ?";
             
             PreparedStatement pst = con.prepareStatement(sql);
             pst.setInt(1, bookId);
             
             int rowCount = pst.executeUpdate();
             
             if (rowCount > 0) {
                 
               JOptionPane.showMessageDialog(this, "Book Count Updated");
               
             }
             
             else
             {
                 JOptionPane.showMessageDialog(this, "Book COunt Updation Failed");
             }
         } catch (Exception e) {
             e.printStackTrace();
         }
     }
     
